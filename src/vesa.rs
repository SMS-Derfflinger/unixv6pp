struct VesaConsole {
    initialized: bool,
    mem: usize,
    width: usize,
    screen_height: usize,
    height: usize,
    pitch: usize,
    bytes_per_pixel: usize,
    curr_x: usize,
    curr_y: usize,
    input_begin: usize,
}

impl VesaConsole {
    const fn new() -> Self {
        Self {
            initialized: false,
            mem: VESA_SCREEN_VADDR,
            width: 0,
            screen_height: 0,
            height: 0,
            pitch: 0,
            bytes_per_pixel: 0,
            curr_x: 0,
            curr_y: 0,
            input_begin: 0,
        }
    }

    fn init(&mut self, mode_info: VbeModeInfo) {
        self.initialized = true;
        self.mem = VESA_SCREEN_VADDR;
        self.width = mode_info.width as usize;
        self.screen_height = mode_info.height as usize;
        self.height = self.screen_height / 2;
        self.pitch = mode_info.pitch as usize;
        self.bytes_per_pixel = (mode_info.bpp as usize).max(8) / 8;
        self.curr_x = 0;
        self.curr_y = 0;
        self.input_begin = 0;
    }

    fn put_char(&mut self, ch: u8) -> bool {
        if !self.initialized {
            return false;
        }

        match ch {
            b'\r' => self.curr_x = 0,
            b'\n' => self.line_feed(),
            b'\t' => self.tab(),
            b'\x08' => self.backspace(),
            0x20..=0x7e => self.draw_visible(ch, DEFAULT_COLOR),
            _ => {}
        }

        true
    }

    fn mark_input_begin(&mut self) -> bool {
        if !self.initialized {
            return false;
        }

        self.input_begin = self.cursor_pos();
        true
    }

    fn line_feed(&mut self) {
        if self.curr_y + 1 >= self.char_rows() {
            self.scroll_down(1);
        } else {
            self.curr_y += 1;
        }
        self.curr_x = 0;
        self.mark_input_begin();
    }

    fn tab(&mut self) {
        let old_x = self.curr_x;
        let next_tab = (old_x & !0x7) + 8;

        if next_tab >= self.char_cols() {
            self.line_feed();
        } else {
            while self.curr_x < next_tab {
                self.draw_visible(b' ', DEFAULT_COLOR);
            }
        }
    }

    fn backspace(&mut self) {
        if self.cursor_pos() == self.input_begin || self.curr_x == 0 {
            return;
        }

        self.curr_x -= 1;
        self.draw_cell(b' ', DEFAULT_COLOR);
    }

    fn draw_visible(&mut self, ch: u8, color: u32) {
        self.draw_cell(ch, color);
        self.curr_x += 1;

        if self.curr_x >= self.char_cols() {
            self.curr_x = 0;
            if self.curr_y + 1 >= self.char_rows() {
                self.scroll_down(1);
            } else {
                self.curr_y += 1;
            }
        }
    }

    fn draw_cell(&mut self, ch: u8, color: u32) {
        let idx = (ch - b' ') as usize;
        let glyph = &FONTS[idx * FONT_CH_HEIGHT..(idx + 1) * FONT_CH_HEIGHT];
        let base_x = self.curr_x * FONT_CH_WIDTH;
        let base_y = self.curr_y * FONT_CH_HEIGHT;

        for (row, byte) in glyph.iter().copied().enumerate() {
            for col in 0..FONT_CH_WIDTH {
                let mask = 1 << (FONT_CH_WIDTH - 1 - col);
                let pixel_color = if byte & mask != 0 { color } else { BLACK };
                self.put_pixel(base_x + col, base_y + row, pixel_color);
            }
        }
    }

    fn scroll_down(&mut self, lines: usize) {
        let rows = self.char_rows();
        if rows == 0 {
            return;
        }

        if lines >= rows {
            self.clear_rect(0, 0, self.width, self.height);
            self.curr_x = 0;
            self.curr_y = 0;
            self.input_begin = 0;
            return;
        }

        let pixel_lines = lines * FONT_CH_HEIGHT;
        let copy_height = self.height - pixel_lines;

        for y in 0..copy_height {
            for x in 0..self.width {
                let color = self.read_pixel(x, y + pixel_lines);
                self.put_pixel(x, y, color);
            }
        }

        self.clear_rect(0, copy_height, self.width, pixel_lines);
        self.curr_y = self.curr_y.saturating_sub(lines);
        self.input_begin = self.cursor_pos();
    }

    fn clear_rect(&mut self, x0: usize, y0: usize, width: usize, height: usize) {
        for y in y0..y0 + height {
            for x in x0..x0 + width {
                self.put_pixel(x, y, BLACK);
            }
        }
    }

    fn clear_screen(&mut self, color: u32) -> bool {
        if !self.initialized {
            return false;
        }

        for y in 0..self.screen_height {
            for x in 0..self.width {
                self.put_pixel(x, y, color);
            }
        }
        self.curr_x = 0;
        self.curr_y = 0;
        self.input_begin = 0;
        true
    }

    fn put_screen_pixel(&mut self, x: usize, y: usize, color: u32) -> bool {
        if !self.initialized {
            return false;
        }

        self.put_pixel(x, y, color);
        true
    }

    fn put_pixel(&mut self, x: usize, y: usize, color: u32) {
        if x >= self.width || y >= self.screen_height {
            return;
        }

        let offset = y * self.pitch + x * self.bytes_per_pixel;
        let pixel = (self.mem + offset) as *mut u8;
        unsafe {
            pixel.write_volatile((color & 0xff) as u8);
            if self.bytes_per_pixel > 1 {
                pixel.add(1).write_volatile(((color >> 8) & 0xff) as u8);
            }
            if self.bytes_per_pixel > 2 {
                pixel.add(2).write_volatile(((color >> 16) & 0xff) as u8);
            }
            if self.bytes_per_pixel > 3 {
                pixel.add(3).write_volatile(0);
            }
        }
    }

    fn read_pixel(&self, x: usize, y: usize) -> u32 {
        let offset = y * self.pitch + x * self.bytes_per_pixel;
        let pixel = (self.mem + offset) as *const u8;

        unsafe {
            let b = pixel.read_volatile() as u32;
            let g = if self.bytes_per_pixel > 1 {
                pixel.add(1).read_volatile() as u32
            } else {
                0
            };
            let r = if self.bytes_per_pixel > 2 {
                pixel.add(2).read_volatile() as u32
            } else {
                0
            };
            b | (g << 8) | (r << 16)
        }
    }

    fn cursor_pos(&self) -> usize {
        self.curr_y * self.char_cols() + self.curr_x
    }

    fn char_cols(&self) -> usize {
        self.width / FONT_CH_WIDTH
    }

    fn char_rows(&self) -> usize {
        self.height / FONT_CH_HEIGHT
    }
}

static VESA_CONSOLE: LazyLock<SuperCell<VesaConsole>> =
    LazyLock::new(|| SuperCell::new(VesaConsole::new()));

pub fn write_output_byte(ch: u8) -> bool {
    VESA_CONSOLE.with_mut(|console| console.put_char(ch))
}

pub fn mark_input_begin() -> bool {
    VESA_CONSOLE.with_mut(VesaConsole::mark_input_begin)
}

#[no_mangle]
pub extern "C" fn vesa_clear(color: i32) {
    VESA_CONSOLE.with_mut(|console| {
        console.clear_screen(color as u32);
    });
}

#[no_mangle]
pub extern "C" fn vesa_put_pixel(x: i32, y: i32, color: i32) {
    if x < 0 || y < 0 {
        return;
    }

    VESA_CONSOLE.with_mut(|console| {
        console.put_screen_pixel(x as usize, y as usize, color as u32);
    });
}

#[no_mangle]
pub unsafe extern "C" fn vesa_init(mode_info: *const VbeModeInfo) {
    if mode_info.is_null() {
        return;
    }

    let mode_info = core::ptr::read_unaligned(mode_info);
    VESA_CONSOLE.with_mut(|console| console.init(mode_info));
}
